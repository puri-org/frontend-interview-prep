// Напиши функцию throttle(), которая принимает два аргумента:
//
//     fn: функцию, которая должна вызываться не чаще, чем один раз за заданный интервал.
//     delay: задержка в миллисекундах, которая ограничивает частоту вызова функции.

const throttledFn = throttle(() => console.log("Function called"), 1000);
throttledFn();  // "Function called"
throttledFn();  // Игнорируется (пока не пройдет 1 секунда)

